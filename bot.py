# –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –∞–∫—Ç—É–∞–ª—å–Ω—ã—Ö —Ö—ç—à—Ç–µ–≥–æ–≤
def generate_hashtags(topic):
    logger.info(f"–ì–µ–Ω–µ—Ä–∞—Ü–∏—è —Ö—ç—à—Ç–µ–≥–æ–≤ –¥–ª—è —Ç–µ–º—ã: {topic}")
    words = topic.split()
    base_hashtags = [f"#{word}" for word in words if len(word) > 2]
    thematic_hashtags = {
        "–º–∞—Ä–∫–µ—Ç–∏–Ω–≥": ["#–ø—Ä–æ–¥–≤–∏–∂–µ–Ω–∏–µ", "#–±—Ä–µ–Ω–¥", "#—Ä–µ–∫–ª–∞–º–∞", "#smm—Å—Ç—Ä–∞—Ç–µ–≥–∏—è", "#–º–∞—Ä–∫–µ—Ç–æ–ª–æ–≥", "#–∫–æ–Ω–≤–µ—Ä—Å–∏—è"],
        "–∫–æ—Ñ–µ": ["#–∫–æ—Ñ–µ–º–∞–Ω–∏—è", "#—É—Ç—Ä–æ", "#–Ω–∞–ø–∏—Ç–∫–∏", "#–∫–æ—Ñ–µ–π–Ω—è", "#–±–æ–¥—Ä–æ—Å—Ç—å", "#–∫–æ—Ñ–µ—ë–∫"],
        "–ø–∞—Ä–∏–∫–º–∞—Ö–µ—Ä": ["#—Å—Ç—Ä–∏–∂–∫–∞", "#—É—Ö–æ–¥–∑–∞–≤–æ–ª–æ—Å–∞–º–∏", "#—Å—Ç–∏–ª—å", "#–∫—Ä–∞—Å–æ—Ç–∞", "#–º–æ–¥–∞", "#–ø–∞—Ä–∏–∫–º–∞—Ö–µ—Ä—Å–∫–æ–µ–∏—Å–∫—É—Å—Å—Ç–≤–æ"]
    }
    relevant_tags = []
    for key in thematic_hashtags:
        if key in topic.lower():
            relevant_tags.extend(thematic_hashtags[key])
            break
    if not relevant_tags:
        relevant_tags = ["#—Å–æ—Ü—Å–µ—Ç–∏", "#—Å–æ–¥–µ—Ä–∂–∞–Ω–∏–µ", "#–∏–¥–µ–∏", "#–ø–æ–ª–µ–∑–Ω–æ", "#–∂–∏–∑–Ω—å"]
    
    combined = list(set(base_hashtags + relevant_tags))
    combined.sort(key=lambda x: (len(x), x in topic.lower()), reverse=True)
    corrected_tags = [correct_text(tag[1:]) for tag in combined[:8]]
    final_tags = [f"#{tag}" for tag in corrected_tags] + ["#–∏–Ω—Å—Ç–∞–≥—Ä–∞–º", "#–≤–∫–æ–Ω—Ç–∞–∫—Ç–µ", "#—Ç–µ–ª–µ–≥—Ä–∞–º"]
    return " ".join(final_tags)

